import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const skillsRoot = path.join(root, 'skills');
const outputFile = path.join(root, 'src', 'skills', 'embedded-repo-skills.ts');

const bundle = await buildBundle(skillsRoot);
const content = renderBundle(bundle);
await writeFile(outputFile, content, 'utf8');
console.log(`Embedded repo skills written to ${outputFile}`);

async function buildBundle(rootDir) {
  const entries = await readdir(rootDir, { withFileTypes: true });
  const skillDirs = entries
    .filter(entry => entry.isDirectory())
    .map(entry => entry.name)
    .sort((left, right) => left.localeCompare(right));

  const result = {};
  for (const skillName of skillDirs) {
    const skillDir = path.join(rootDir, skillName);
    result[skillName] = await readDirectoryFiles(skillDir, skillDir);
  }
  return result;
}

async function readDirectoryFiles(baseDir, currentDir) {
  const entries = await readdir(currentDir, { withFileTypes: true });
  const sortedEntries = [...entries].sort((left, right) => left.name.localeCompare(right.name));
  const result = {};

  for (const entry of sortedEntries) {
    const fullPath = path.join(currentDir, entry.name);
    if (entry.isDirectory()) {
      const nested = await readDirectoryFiles(baseDir, fullPath);
      Object.assign(result, nested);
      continue;
    }
    if (!entry.isFile()) continue;
    const relativePath = path.relative(baseDir, fullPath).split(path.sep).join('/');
    result[relativePath] = await readFile(fullPath, 'utf8');
  }

  return result;
}

function renderBundle(bundle) {
  return `// Copyright (c) 2026 ByteDance Ltd. and/or its affiliates
// SPDX-License-Identifier: Apache-2.0
// AUTO-GENERATED by scripts/generate-embedded-repo-skills.mjs. Do not edit manually.

export interface EmbeddedRepoSkillBundle {
  [skillName: string]: Record<string, string>;
}

export const EMBEDDED_REPO_SKILLS: EmbeddedRepoSkillBundle = ${JSON.stringify(bundle, null, 2)};\n`;
}
